                <p class="text-justify text-[15px] mb-3">Schedule I of the District Council Act lists the functions of
                    the Council. It lists 123 functions. Broadly speaking, it can be said that the important functions
                    of the Council are to provide services such as education, health, communication and to undertake
                    programs for the development of agriculture and rural industries. It is expected that the expansion
                    of education and communication programs should be done in such a way that it will be conducive to
                    the development of agriculture and industries and that emphasis should be placed on programmes such
                    as irrigation and cooperation. It is also considered a special responsibility of the Zilla Parishad
                    to pay attention to the upliftment of the Scheduled Castes and Scheduled Tribes. The above-mentioned
                    functions are also within the purview of the State Government. Therefore, the functions have been
                    divided between the State Government and the Zilla Parishad and are subject to changes as required.
                    For example, primary education has been completely entrusted to the Council. The Council carries out
                    the functions of construction of school buildings, supervision of the work of teachers, etc. The
                    management of primary schools in cities that are municipalities but do not have school boards is
                    also entrusted to the councils. Although the education officer of the council does the inspection of
                    secondary schools and the release of grant money, the Zilla Parishad cannot take any notice of it.
                    Regarding communication, the construction and maintenance of national and state highways is with the
                    state government, while the construction and maintenance of district roads, small access roads, etc.
                    is with the council. In the irrigation scheme, the council can take up projects that will provide
                    water to land up to one hundred hectares, while projects larger than that come under the purview of
                    the state government.</p>
