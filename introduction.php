                <p><strong>Panchayat Raj is an innovative experiment in the making of modern India.</strong></p>
                <p class="text-justify">The Balwantrai Mehta Committee (1958) recommended a three-tier Panchayat Raj
                    system to involve the rural masses in rural development. Accordingly, the Government of Maharashtra
                    dated On May 1, 1962, the Maharashtra Zilla Parishad and Panchayat Samiti Act, 1961 were agreed upon
                    and the Zilla Parishad at the district level, Panchayat Samiti at the group level and Gram Panchayat
                    at the village level established a three-tier Panchayat Raj. In this way, the state of Maharashtra
                    became the ninth state in the country to adopt the Panchayat Raj system. A Zilla Parishad (commonly
                    known as Zilla Parishad) is a local government body at the district level in India. Generally the
                    Zilla Parishad office is located at the District Headquarters. Zilla Parishad works to convey the
                    information and services of various schemes of Maharashtra and Central Government to the common
                    people in rural areas.</p>

                <p><strong>Basic Functions of Zilla Parishad:</strong></p>
